package Control_structures;

public class ConditionalConundrum {

	public static void main(String[] args) {
		
		
		int var = 90;
		if(var>0)
		{
			
			System.out.println(var+" is positive");
		}
		else if(var==0)
		{
			System.out.println(var+" is zero");
		}
		else
		{
			System.out.println(var+" is Negative");
			
		}
		
	
	}

}
