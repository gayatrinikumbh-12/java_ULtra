package JAVA_Beginner;

public class RightTypeRiddle {

	public static void main(String[] args) {
			int a;
			char b;
			double c;
			boolean d;
			
			a= 100;
			b= 'c';
			c= 8.77;
			d=false;
			System.out.println("INTEGER "+a);
			System.out.println("CHAR "+b);
			System.out.println("DOUBLE "+c);
			System.out.println("BOOLEAN "+d);
	}

}
