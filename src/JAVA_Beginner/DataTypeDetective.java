package JAVA_Beginner;

public class DataTypeDetective {

	public static void main(String[] args) {
		
		int a =70;
		double b=56.77;
		char v = 't';
		boolean n = true;
		
		System.out.println(a+" is a Integer");
		System.out.println(b +" is a double");
		System.out.println(v+" is a char");
		System.out.println(n+" is a boolean");
		
	}

}
