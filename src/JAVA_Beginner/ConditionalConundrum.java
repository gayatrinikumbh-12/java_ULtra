package JAVA_Beginner;

public class ConditionalConundrum {

	public static void main(String[] args) {
	
		int num = -9;
		if(num>0)
		{
			System.out.println(num+" is Positive");
		}
		else if (num==0)
		{
			System.out.println(num+" is Zero");
		}
		else
		{
			System.out.println(num+" is Negative");
		}
		
	}

}
